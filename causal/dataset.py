"""
Causal intervention dataset for CausalVoice.

Loads pre-generated intervention groups from generate_interventions.py.
Each group is a single .pt file: {items: [tensor(T,768), ...], causal_effects: tensor([...])}
"""

import os
import random
import numpy as np
import torch
import torch.utils.data
from pathlib import Path


class CausalInterventionDataset(torch.utils.data.Dataset):
    """
    Loads causal intervention groups generated by generate_interventions.py.
    
    Each group file (group_XXXXX.pt) contains:
        - items: list of tensors [(T, feat_dim), ...] where items[0] is anchor
        - causal_effects: tensor of (n_interventions,) float values
    """
    
    def __init__(self, data_path, hparams=None, max_interventions=8):
        self.data_path = Path(data_path)
        self.max_interventions = max_interventions
        
        # Find all group files
        self.group_files = sorted(self.data_path.glob("group_*.pt"))
        
        if len(self.group_files) == 0:
            raise ValueError(f"No group_*.pt files found in {data_path}")
        
        print(f"CausalInterventionDataset: {len(self.group_files)} groups from {data_path}")
    
    def __len__(self):
        return len(self.group_files)
    
    def __getitem__(self, index):
        """
        Returns:
            features: list of (feat_dim, T) tensors [anchor, intervention_0, ...]
            causal_effects: tensor of (n_interventions,)
        """
        data = torch.load(self.group_files[index], map_location='cpu', weights_only=False)
        
        items = data['items']           # list of (T, feat_dim) tensors
        causal_effects = data['causal_effects']  # (n_interventions,)
        
        # Transpose to (feat_dim, T) for conv1d compatibility
        features = []
        for item in items[:self.max_interventions + 1]:
            if item.dim() == 2:
                features.append(item.T)   # (T, D) → (D, T)
            else:
                features.append(item)
        
        # Trim causal effects to match
        n_interventions = len(features) - 1  # exclude anchor
        causal_effects = causal_effects[:n_interventions].float()
        
        return features, causal_effects


def causal_collate_fn(batch):
    """
    Collate causal intervention batches.
    
    Flattens all groups into a single batch with split indices.
    
    Returns:
        all_features: (N_total, feat_dim, T_max) padded features
        causal_effects: list of tensors per group
        data_splits: list of cumulative indices [0, n1, n1+n2, ...]
    """
    all_features = []
    causal_effects = []
    data_splits = [0]
    
    for features, ce in batch:
        all_features.extend(features)
        causal_effects.append(ce)
        data_splits.append(data_splits[-1] + len(features))
    
    # Pad features to same time length
    feat_dim = all_features[0].size(0)
    max_len = max(f.size(-1) for f in all_features)
    
    padded_features = torch.zeros(len(all_features), feat_dim, max_len)
    for i, f in enumerate(all_features):
        padded_features[i, :, :f.size(-1)] = f
    
    return padded_features, causal_effects, data_splits
